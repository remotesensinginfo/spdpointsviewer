        SPDPointsViewerColourTable* createColourTabHue()
        {
            // Create HUE Colour Table.
            SPDPointsViewerColourTable *hueColourTab = new SPDPointsViewerColourTable();
            hueColourTab->setName("Hue");
            float hue = 0;
            QColor color;
            for(unsigned int i = 0; i < 256; ++i)
            {
                ClrVals clrVal;
                clrVal.val = i;
                hue = ((float)i)/255;
                color.setHsvF(hue, 1.0, 1.0, 1.0);
                
                clrVal.red = color.redF();
                clrVal.green = color.greenF();
                clrVal.blue = color.blueF();
                hueColourTab->addColorValPair(clrVal);
            }
            return hueColourTab;
        };
        
        SPDPointsViewerColourTable* createColourTabGreyscale()
        {
            // Create Greyscale Colour Table.
            SPDPointsViewerColourTable *greyColourTab = new SPDPointsViewerColourTable();
            greyColourTab->setName("Greyscale");
            float floatHSVClrVal = 0;
            QColor color;
            for(unsigned int i = 0; i < 256; ++i)
            {
                ClrVals clrVal;
                clrVal.val = i;
                floatHSVClrVal = 1-(((float)i)/255);
                color.setHsvF(0, 0, floatHSVClrVal, 1.0);													
                
                clrVal.red = color.redF();
                clrVal.green = color.greenF();
                clrVal.blue = color.blueF();
                greyColourTab->addColorValPair(clrVal);
            }
            
            return greyColourTab;
        };
        
